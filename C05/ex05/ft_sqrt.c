int	ft_sqrt(int nb)
{
	int sqrt;

	sqrt = 0;
	if (nb > 0)
	{
		while ((sqrt * sqrt != nb) && (sqrt < nb) && (sqrt < 46340))
			sqrt++;
	}
	if (sqrt * sqrt != nb)
		return (0);
	else
		return (sqrt);
}
